<div class="info">
  <p><strong>TheThe  Site  Exit  Manager</strong> is a WordPress plugin that manages the event of
    web site exit. When a visitor tries to quit the web site, this plugin pops up a special
    window  with  text to ask the visitor  if she/he really wants to quit. </p>
  <p> While showing the window, the plugin is loading a specified URL or WP page/post on background
    (usually with a special offer  to  motivate  the  visitor to keep surfing the site). When used
    wisely, the TTSEM plugin can help increase your sales letter or squeeze page conversion rate. </p>
